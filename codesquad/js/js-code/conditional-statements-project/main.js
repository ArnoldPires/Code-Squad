var years = prompt('How many years have you been programming with JavaScript?');
if (years >= 5) {
  alert("You're an expert");
}else if (years == 1) {
  alert("You're getting better");
}else if (years = 5 | 2) {
  alert("You've got solid experience");
}else if (years <= 0) {
  alert('Start learning by clicking the link on this page!');
}
