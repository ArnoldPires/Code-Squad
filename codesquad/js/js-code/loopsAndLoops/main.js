//1  loop that counts from 0 to 10
for (let x=0; x< 11; x++) {
    console.log ("The number is " + x);
}
//2  a loop that counts from 5 to 15 in the Console, in multiples of 3
for (let x=5; x<=15; x+=3) {
    console.log(x);
}
//3 a loop that counts by two, from 40 to 60
for (x=40; x <= 60 ; x+=2) {
    alert(x + "\n");
}
//4  a loop that counts backwards from 100 to 0
for (i=100; i>=0; i--){
    console.log(i);
}
